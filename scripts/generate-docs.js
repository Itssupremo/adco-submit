function watchDocs() {
  const watchTargets = [
    path.join(root, 'backend'),
    path.join(root, 'frontend'),
    path.join(root, 'api'),
    path.join(root, 'package.json'),
    path.join(root, 'vercel.json'),
  ];

  let timer = null;
  const schedule = () => {
    clearTimeout(timer);
    timer = setTimeout(() => {
      try {
        generateDocs();
      } catch (error) {
        console.error('Documentation generation failed:', error);
      }
    }, 250);
  };

  generateDocs();
  console.log('Watching for documentation-relevant changes...');

  for (const target of watchTargets) {
    if (!fs.existsSync(target)) continue;
    const stat = fs.statSync(target);
    if (stat.isDirectory()) {
      fs.watch(target, { recursive: true }, (eventType, filename) => {
        if (!filename) return;
        if (/node_modules|dist|Iangwapo|\.git/.test(filename)) return;
        schedule();
      });
    } else {
      fs.watchFile(target, { interval: 500 }, schedule);
    }
  }
}

if (process.argv.includes('--watch')) {
  watchDocs();
} else {
  generateDocs();
}