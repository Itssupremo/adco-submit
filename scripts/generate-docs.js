function ensureDir(dir) {
  fs.mkdirSync(dir, { recursive: true });
}

function readText(filePath) {
  return fs.readFileSync(filePath, 'utf8');
}

function readJson(filePath) {
  return JSON.parse(readText(filePath));
}

function writeDoc(relPath, content) {
  const filePath = path.join(root, relPath);
  ensureDir(path.dirname(filePath));
  fs.writeFileSync(filePath, content.trimEnd() + '\n', 'utf8');
}

function rel(filePath) {
  return path.relative(root, filePath).replace(/\\/g, '/');
}

function listFiles(dir, options = {}) {
  const { includeExt = null, excludeDirs = [] } = options;
  const results = [];

  function walk(current) {
    const entries = fs.readdirSync(current, { withFileTypes: true });
    for (const entry of entries) {
      const full = path.join(current, entry.name);
      if (entry.isDirectory()) {
        if (excludeDirs.includes(entry.name)) continue;
        walk(full);
      } else {
        if (includeExt && !includeExt.includes(path.extname(entry.name))) continue;
        results.push(full);
      }
    }
  }

  walk(dir);
  return results.sort();
}

function listDirNames(dir) {
  return fs.readdirSync(dir).sort();
}

function extractObjectBlock(text, marker) {
  const start = text.indexOf(marker);
  if (start === -1) return '';
  const braceStart = text.indexOf('{', start);
  if (braceStart === -1) return '';
  let depth = 0;
  for (let i = braceStart; i < text.length; i++) {
    if (text[i] === '{') depth += 1;
    if (text[i] === '}') depth -= 1;
    if (depth === 0) {
      return text.slice(braceStart + 1, i);
    }
  }
  return '';
}

function parseTopLevelKeys(block) {
  const keys = [];
  let depth = 0;
  let buffer = '';
  let readingKey = true;

  for (let i = 0; i < block.length; i++) {
    const ch = block[i];
    if (ch === '{' || ch === '[' || ch === '(') depth += 1;
    if (ch === '}' || ch === ']' || ch === ')') depth -= 1;

    if (depth === 0 && readingKey) {
      if (ch === ':') {
        const key = buffer.trim().replace(/,$/, '');
        if (key && !key.startsWith('//')) keys.push(key);
        buffer = '';
        readingKey = false;
        continue;
      }
      if (ch === ',') {
        buffer = '';
        continue;
      }
      buffer += ch;
    } else if (depth === 0 && !readingKey && ch === ',') {
      readingKey = true;
    }
  }

  return keys
    .map((key) => key.split('\n').pop().trim())
    .filter(Boolean)
    .map((key) => key.replace(/['"`]/g, ''));
}

function parseModel(filePath) {
  const text = readText(filePath);
  const mainSchemaMatch = text.match(/const\s+([A-Za-z0-9_]+Schema)\s*=\s*new\s+mongoose\.Schema\(/);
  const modelName = path.basename(filePath, '.js');
  if (!mainSchemaMatch) {
    return { modelName, fields: [], indexes: [] };
  }

  const schemaName = mainSchemaMatch[1];
  const block = extractObjectBlock(text, `const ${schemaName} = new mongoose.Schema(`);
  const fields = parseTopLevelKeys(block);
  const indexMatches = [...text.matchAll(/\.index\(([^\n]+)\)/g)].map((m) => m[1].trim());
  return { modelName, fields, indexes: indexMatches, source: rel(filePath) };
}

function parseRoutes(filePath) {
  const text = readText(filePath);
  const lines = text.split(/\r?\n/);
  const base = routeBaseMap[path.basename(filePath)] || '/api';
  const routes = [];
  for (const line of lines) {
    const match = line.match(/router\.(get|post|put|delete|patch)\(([^,]+),\s*(.+)\);?/i);
    if (!match) continue;
    const method = match[1].toUpperCase();
    const rawPath = match[2].trim().replace(/['"`]/g, '');
    const middlewareChain = match[3];
    let auth = 'Public';
    if (/authenticate/.test(middlewareChain)) auth = 'Authenticated';
    if (/adminOrAbove|superAdminOnly|adminOnly/.test(middlewareChain)) auth = 'Admin or Above';
    const fullPath = `${base}${rawPath === '/' ? '' : rawPath}`;
    routes.push({ method, path: fullPath, auth, source: rel(filePath) });
  }
  return routes;
}

function parseEnvUsage() {
  const backendFiles = listFiles(path.join(root, 'backend'), {
    includeExt: ['.js'],
    excludeDirs: ['node_modules'],
  });
  const frontendFiles = listFiles(path.join(root, 'frontend'), {
    includeExt: ['.js', '.jsx'],
    excludeDirs: ['node_modules', 'dist'],
  });

  const backendVars = new Set();
  const frontendVars = new Set();

  for (const file of backendFiles) {
    const text = readText(file);
    for (const match of text.matchAll(/process\.env\.([A-Z0-9_]+)/g)) {
      backendVars.add(match[1]);
    }
  }
  for (const file of frontendFiles) {
    const text = readText(file);
    for (const match of text.matchAll(/import\.meta\.env\.([A-Z0-9_]+)/g)) {
      frontendVars.add(match[1]);
    }
  }

  return {
    backend: [...backendVars].sort(),
    frontend: [...frontendVars].sort(),
  };
}

function parseAppRoutes() {
  const appPath = path.join(root, 'frontend', 'src', 'App.jsx');
  const text = readText(appPath);
  const routes = [];
  for (const match of text.matchAll(/<Route\s+path="([^"]+)"/g)) {
    routes.push(match[1]);
  }
  return [...new Set(routes)];
}

function makeTree(dir, depth = 2, prefix = '') {
  const entries = fs.readdirSync(dir, { withFileTypes: true }).sort((a, b) => a.name.localeCompare(b.name));
  const lines = [];
  for (const entry of entries) {
    if (['node_modules', '.git', 'dist'].includes(entry.name)) continue;
    const suffix = entry.isDirectory() ? '/' : '';
    lines.push(`${prefix}${entry.name}${suffix}`);
    if (entry.isDirectory() && depth > 0) {
      lines.push(...makeTree(path.join(dir, entry.name), depth - 1, `${prefix}  `));
    }
  }
  return lines;
}

function mdList(items) {
  return items.map((item) => `- ${item}`).join('\n');
}

function mdTable(headers, rows) {
  const head = `| ${headers.join(' | ')} |`;
  const sep = `| ${headers.map(() => '---').join(' | ')} |`;
  const body = rows.map((row) => `| ${row.join(' | ')} |`).join('\n');
  return [head, sep, body].filter(Boolean).join('\n');
}

